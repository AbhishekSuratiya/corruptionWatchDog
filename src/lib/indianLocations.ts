// Combine all locations for search
export const ALL_INDIAN_LOCATIONS = [
  ...INDIAN_STATES,
  ...MAJOR_INDIAN_CITIES
].sort();

// Function to filter locations based on search term
export const filterLocations = (searchTerm: string, limit: number = 10): string[] => {
  if (!searchTerm || searchTerm.length < 1) {
    return ALL_INDIAN_LOCATIONS.slice(0, limit);
  }

  const lowerSearchTerm = searchTerm.toLowerCase();
  
  // First, find exact matches at the beginning
  const exactMatches = ALL_INDIAN_LOCATIONS.filter(location =>
    location.toLowerCase().startsWith(lowerSearchTerm)
  );

  // Then, find partial matches
  const partialMatches = ALL_INDIAN_LOCATIONS.filter(location =>
    !location.toLowerCase().startsWith(lowerSearchTerm) &&
    location.toLowerCase().includes(lowerSearchTerm)
  );

  // Combine and limit results
  return [...exactMatches, ...partialMatches].slice(0, limit);
};

